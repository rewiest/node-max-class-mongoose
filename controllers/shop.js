exports.getInvoice = (req, res, next) => {
  const orderId = req.params.orderId;
  Order
    .findById(orderId)
    .then(order => {
      if (!order) {
        return next(new Error('No order found.'));
      }
      if (order.user.userId.toString() !== req.user._id.toString()) {
        return next(new Error('Not authorized!'));
      }
      const invoiceName = 'invoice-' + orderId + '.pdf';
      const invoicePath = path.join('data', 'invoices', invoiceName);
      
      // read entire file before sending via response to download file
      // fs.readFile(invoicePath, (err, data) => {
      //   if (err) {
      //     return next(err);
      //   }
      //   res.setHeader('Content-Type', 'application/pdf');
      //   res.setHeader('Content-Disposition', 'attachment; filename="' + invoiceName + '"');
      //   res.send(data);
      // });
      
      // read entire file before sending via response to view file in browser
      // fs.readFile(invoicePath, (err, data) => {
      //   if (err) {
      //     return next(err);
      //   }
      //   res.setHeader('Content-Type', 'application/pdf');
      //   res.setHeader('Content-Disposition', 'inline; filename="' + invoiceName + '"');
      //   res.send(data);
      // });

      // stream file and pipe output to response to view file in browser - recommended over reading in entire file
      // const file = fs.createReadStream(invoicePath);
      // res.setHeader('Content-Type', 'application/pdf');
      // res.setHeader('Content-Disposition', 'inline; filename="' + invoiceName + '"');
      // file.pipe(res);

      // create PDF document on the fly and then stream and pipe output to reponse to view file in browser
      const pdfDoc = new PDFDocument();
      res.setHeader('Content-Type', 'application/pdf');
      res.setHeader('Content-Disposition', 'inline; filename="' + invoiceName + '"');
      pdfDoc.pipe(fs.createWriteStream(invoicePath));
      pdfDoc.pipe(res);
      pdfDoc.fontSize(26).text('Invoice', { underline: true });
      pdfDoc.text(' ');
      let totalPrice = 0;
      order.products.forEach(prod => {
        pdfDoc.fontSize(14).text(prod.product.title + '  -  Qty: ' + prod.quantity + ' x $' + prod.product.price);
        totalPrice = totalPrice + prod.quantity * prod.product.price;
      })
      pdfDoc.text(' ');
      pdfDoc.fontSize(20).text('Total Price: $' + totalPrice);
      pdfDoc.end();

    })
    .catch(err => {
      next(err);
    });
  
}

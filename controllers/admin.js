      //   hasError: true,
      //   product: {
      //     title: title,
      //     price: price,
      //     imageUrl: imageUrl,
      //     description: description
      //   },
      //   errorMessage: 'Database failure. Please try again.',
      //   validationErrors: []
      // });
      //
      // OPTION 2 - RETURN A NEW GENERAL ERROR PAGE
      // res.redirect('/error/general-error');
      //
      // OPTION 3 - THROW THE ERROR VIA NEXT() TO BE HANDLED BY EXPRESS
